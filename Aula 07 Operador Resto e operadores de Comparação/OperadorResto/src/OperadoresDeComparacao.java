public class OperadoresDeComparacao 
{
    public static void main(String[] args) 
    {        
        boolean MaiorQue = 10 > 20;
        System.out.println(MaiorQue);

        boolean MenorQue = 10 < 20;
        System.out.println(MenorQue);
        
        boolean MaiorIgualQue = 10 >= 20;
        System.out.println(MaiorIgualQue);

        boolean MenorIgualQue = 10 <= 20;
        System.out.println(MenorIgualQue);

        boolean Igual = 10 == 20;
        System.out.println(Igual);
        
        boolean DiferenteDe = 10 != 20;
        System.out.println(DiferenteDe);
    }
}
