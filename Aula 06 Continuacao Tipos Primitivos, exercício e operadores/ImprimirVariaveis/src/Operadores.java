public class Operadores 
{
    public static void main(String[] args) 
    {
        /***
         * Operadores
         * 1. Operadores Aritméticos
         * 
         * Usados para cálculos matemáticos básicos
         * 
         * +  : Soma ou Concatenação
         * -  : Subtração
         * *  : Multiplicação
         * /  : Divisão
         * %  : Resto da divisão
         * 
         * 2. Operadores Unários
         * Operam em apenas um único operando. 
         * 
         * ++ : Incremento (aumenta o valor em 1).
         * -- : Decremento (diminui o valor em 1).
         * +  : Unário positivo (indica valor positivo).
         * -  : Unário negativo (inverte o sinal do operando).
         * !  : Negação lógica (inverte o valor booleano
         * 
         * 3. Operadores Relacionais (Comparação)
         * 
         * Comparam dois valores e retornam um resultado booleano (true ou false). 
         * 
         * == : Igual a.
         * != : Diferente de.
         * >  : Maior que.
         * <  : Menor que.
         * >= : Maior ou igual a.
         * <= : Menor ou igual a. 
         * 
         * 4. Operadores Lógicos
         * 
         * Utilizados para combinar expressões booleanas. 
         * 
         * && : E lógico (AND) — retorna verdadeiro se ambos forem verdadeiros.
         * || : OU lógico (OR) — retorna verdadeiro se pelo menos um for verdadeiro. 
         * 
         * 5. Operadores de Atribuição
         * 
         * Atribuem valores a variáveis. 
         * 
         * = : Atribuição simples.
         * +=, -=, *=, /=, %= : Atribuições compostas (ex: x += 5 é o mesmo que x = x + 5).
         * 
         * 6. Operadores de Bits (Bitwise)
         * 
         * Realizam operações diretamente nos bits dos dados. 
         * 
         * & : AND bit a bit.
         * | : OR bit a bit.
         * ^ : XOR (OU exclusivo) bit a bit.
         * ~ : Complemento bit a bit.
         * << : Deslocamento à esquerda.
         * >> : Deslocamento à direita.
         * >>> : Deslocamento à direita preenchendo com zeros. 
         * 
         * 7. Outros Operadores Importantes
         * 
         * ?: : Operador Ternário — uma forma compacta de if-else.
         * instanceof :  : Verifica se um objeto é uma instância de uma classe específica.
         * (tipo) : Cast — converte um valor de um tipo para outro.
         * 
         *  */    
    }
}
